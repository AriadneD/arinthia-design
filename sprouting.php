          Takeaways
        </h2>
        <p>
          The challenge of this project was to verify smooth compatibility between the English and Spanish versions of the site, to ensure that the target users would have a great experience. This was achieved by collecting several rounds of iterative feedback from user interviews.
<br/><br/>
I learned the importance of cross-functional compatibility throughout this project, as it was truly valuable to be able to learn about creating a great UX experience which diverse users from outside of a typical US audience can enjoy.



