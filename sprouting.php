        <h2>
          Summary
        </h2>
        <p>
Sprouting is an MIT-based ed-tech startup. Their product is Microconcept centered STEM curriculum for teachers in Puerto Rican schools. Their novel method increases class attendance and participation, and decreases time taken to prepare lessons.
<br /><br />
Sprouting wanted a digital interface to collect sign-ups from schools for their pilot program in a way which caters to their bilingual English/Spanish speaking audience. They also needed help scoping out the product requirements.



          <br /><br />
        </p>

        <h2>
          Process
        </h2>
        <p>
          My responsibilities included Mobile UX Designer, Brand Identity Designer, Product Designer and Web Developer. I contributed to the design direction of the website and built the prototype. I conducted user interviews, collected feedback, and helped influence requirements for the product and roadmap. I created mockups for the product mobile app. I also contributed to the logo and brand identity of the company.<br/><br/>

<br /><br />From the results of our user interviews, we came up with three core solutions to our users' pain points. I compiled these assets into a graphic that conveys the value proposition of the product.


