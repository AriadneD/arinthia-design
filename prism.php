        <p>
Prism is a conceptual Computer vision and AI powered camera for your fridge that saves you time deciding what to cook while mitigating waste from expired food. 
The camera is paired to an app so that you can view a list of what’s in your fridge at all times, and yes Prism alerts you when food is about to expire. 
Prism autosuggests recipes based on ingredients you have-- you can decide what to cook in seconds. Also, Prism learns which ingredients you use most through AI, and generates shopping lists to restock when you're running low. 

The idea for Prism arose from some pain points my team discovered by surveying the MIT community. My team surveyed 118 MIT students and recent graduates: Over 80 of them mention a barrier to them cooking at home is a lack of time and they frequently struggle with having to throw out uneaten, forgotten expired food. And 96 of them said they’d be willing to pay for a product that solves these issues for them.
          <br /><br />

          My role was Co-founder and Product Designer. I designed a 3D CAD prototype of the camera and built models to demonstrate camera and fridge placement. I conducted user interviews, collected feedback, and helped influence requirements for the product and roadmap. I created mockups for the product mobile app. I also contributed to the logo and brand identity of the company.<br/><br/>

<br /><br />
