        <h2>
          Takeaways
        </h2>
        <p>
          The challenge of this project was verifying that all of the payment processing and e-commerce components worked smoothly and felt intuitive to the user. This was achieved by conducting user testing and crafting user journey maps to track the positive performance of our e-commerce experience.
<br/><br/>
I learned the importance of iterative user testing throughout this project, as it was truly valuable to be able to test out how our payment system was working by observing user behavior.



