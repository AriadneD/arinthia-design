        <h2>
          Results and Impact
        </h2>
        <p>
          The e-commerce components of this site mean that, for the first time ever, GSA supports secure, fully-online, automated registration where people can purchase access to camps and programs in just a few clicks.
Through SEO search indexing we have increased the reach of GSA to interested parties throughout Maryland. Our website shows up as a first result on Google.
The head coach and several parents of children in the program have praised our design.

